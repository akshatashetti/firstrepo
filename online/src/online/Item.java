package online;

public class Item 
{
	double price1;
	String name1;
	

	public Item(String name) 
	{
		name1=name;
	}
	
	public void setPrice(double price)
	{
		price1=price;
	}
	
	public double getPrice()
	{
		return price1;
	}
	
	public String getName()
	{
		
		return name1;
	}
	
	
	
	
	

}
